package obras;

public class Museu {
    
    int cod_museu;
    String nome_museu;
    String endereco;
    String telefone;

    public int getCod_museu() {
        return cod_museu;
    }

    public void setCod_museu(int cod_museu) {
        this.cod_museu = cod_museu;
    }

    public String getNome_museu() {
        return nome_museu;
    }

    public void setNome_museu(String nome_museu) {
        this.nome_museu = nome_museu;
    }

    public String getEndereco() {
        return endereco;
    }

    public void setEndereco(String endereco) {
        this.endereco = endereco;
    }

    public String getTelefone() {
        return telefone;
    }

    public void setTelefone(String telefone) {
        this.telefone = telefone;
    }
    
}
