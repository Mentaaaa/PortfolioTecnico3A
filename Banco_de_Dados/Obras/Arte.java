package obras;
public class Arte {
    
    int cod_arte;
    int cod_local;
    String nome_arte;
    String data;
    String autor;
    String tipo;

    public int getCod_arte() {
        return cod_arte;
    }

    public void setCod_arte(int cod_arte) {
        this.cod_arte = cod_arte;
    }

    public String getNome_arte() {
        return nome_arte;
    }

    public void setNome_arte(String nome_arte) {
        this.nome_arte = nome_arte;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public String getAutor() {
        return autor;
    }

    public void setAutor(String autor) {
        this.autor = autor;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public int getCod_local() {
        return cod_local;
    }

    public void setCod_local(int cod_local) {
        this.cod_local = cod_local;
    }
    
    
    
    
    
}
